import { readFileSync } from 'node:fs';
import { writeFileSync } from 'node:fs';

const source = readFileSync('dist/modern-monaco.js', 'utf8');
const escaped = JSON.stringify(source);

writeFileSync(
	'packages/obsidian/src/modern-monaco-inline.ts',
	`// Auto-generated by scripts/inline-modern-monaco.mjs\n// Do not edit manually.\nexport const MODERN_MONACO_SOURCE = ${escaped};\n`,
);

console.log(`Inlined modern-monaco.js (${source.length} chars) into modern-monaco-inline.ts`);
